use std::path::Path;

use nes_core::{
    asm::{AddressToLabel, AsmLexer, BytesLabels},
    bus::Bus,
    cpu_6502::Cpu6502,
    mappers::SimpleProgram,
    opcodes::OpCode,
};

pub fn load_cpu<P: AsRef<Path>>(filename: P) -> (Cpu6502, AddressToLabel) {
    let path = filename.as_ref();
    let contents = match std::fs::read_to_string(path) {
        Ok(contents) => contents,
        Err(err) => {
            eprintln!("Unable to read '{}': {}", path.display(), err);
            eprintln!(
                "Make sure the path is correct and points to a readable .asm file."
            );
            std::process::exit(1);
        }
    };
    let mut lexer = AsmLexer::new(&contents);

    match lexer.parse() {
        Ok(_) => {
            let BytesLabels {
                mut bytes,
                address_to_label,
            } = lexer.into_bytes().unwrap();
            bytes.push(OpCode::KIL as u8);
            (
                Cpu6502::new(Bus::new_shared_bus(Box::new(SimpleProgram::load(&bytes)))),
                address_to_label,
            )
        }
        Err(parse_error) => {
            parse_error.panic_nicely();
            panic!("");
        }
    }
}

